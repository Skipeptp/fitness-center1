// Стабильно случайная цитата для дня - чтобы не дёргалась при ререндере
export const getQuoteOfTheDay = () => {
  const dayKey = new Date().toISOString().slice(0, 10);
  let hash = 0;
  for (let i = 0; i < dayKey.length; i++) hash = (hash * 31 + dayKey.charCodeAt(i)) | 0;
  return QUOTES[Math.abs(hash) % QUOTES.length];
};

export const getRandomQuote = () => QUOTES[Math.floor(Math.random() * QUOTES.length)];

